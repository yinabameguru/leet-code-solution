class Solution {
    public Node treeToDoublyList(Node root) {
        Node node = toList(null, root);
        while (node.left != null) {
            node = node.left;
        }
        root = node;
        while (node != null) {
            System.out.println(node.val);
            node = node.right;
        }
        node  = root;
        while (node.right != null) {
            node = node.right;
        }
        while (node != null) {
            System.out.println(node.val);
            node = node.left;
        }
        while (root.left != null) {
            root = root.left;
        }
        return root;
    }

    private Node toList(Node left, Node root) {
        if (root == null) {
            return left;
        }
        left = toList(left, root.left);
        left = convert(left, root);
        return toList(left, root.right);
    }

    private Node convert(Node left, Node root) {
        if (left == null || root == null) {
            return root;
        }
        root.left = left;
        left.right = root;
        return root;
    }
}
